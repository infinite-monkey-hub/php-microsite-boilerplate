    <p><strong><em>"PHP Microsite Boilerplate"</em> es un PHP framework para crear sitios web simples, pero fuertemente funcionales, rápidos y seguros en básicamente todos los entornos.</strong></p>
    <p>La mayoría de los frameworks e incluso las boilerplates requieren un agotador proceso de configuración, donde hay que instalar múltiples dependencias. Esto lleva a una enorme sobrecarga de código, que a menudo no se necesita. Toda esa complejidad es también un riesgo potencial para su sitio web. Además, a menudo no es posible utilizar la mayoría de las soluciones, si se necesita desplegarlo en el plan de alojamiento compartido más barato.</p>
    <p><strong>Este framework/template es diferente y adecuado para ti, si...</strong></p>
    <ul>
      <li>Necesitas construir un sitio web bastante pequeño, con alguna funcionalidad.</li>
      <li>Eliges PHP, porque quieres hacer server-side scripting, mientras que PHP es también tal vez el único lenguaje, que se ejecuta en básicamente todas las opciones de hosting.</li>
      <li>Construyes esta cosa por tu cuenta o con un máximo de una persona más. Esto hace que la mejor práctica, pero la compleja estructura del código sea más una carga innecesaria que un concepto útil.</li>
      <li>Necesitas hacerlo rápido, mientras no quieras hacer compromisos en cuanto a la seguridad o el rendimiento.</li>
    </ul>

    <div class="space50"></div>

    <h2>Key Features</h2>
    <ul>
      <li>Fácil de enrutar.</li>
      <li>Preparado <a href="https://web.dev/progressive-web-apps/" target="_blank" rel="noopener" class="dark_link">Progressive Web App (PWA)</a>.</li>
      <li>Preparado multilenguaje.</li>
      <li><a href="https://directus.io/" target="_blank" rel="noopener" class="dark_link">Directus CMS</a> integración (incl. cache local).</li>
      <li>El GDPR y el CCPA están listos.</li>
      <li>Inteligente serviceworker cache.</li>
      <li>Obtener el soporte de texto para una fácil traducción (+ fallback si no está instalado en el servidor)</li>
      <li>SEO optimizado.</li>
      <li>Generación automática del mapa del sitio.</li>
      <li>Optimizado para Social sharing.</li>
      <li>Amplia documentación en código.</li>
      <li><a href="https://securityheaders.com/" target="_blank" rel="noopener" class="dark_link">Security Headers</a> <span class="small">(.htaccess o a través de PHP).</span></li>
      <li>Múltiples características de seguridad <span class="small">(algunas de ellas requieren un servidor web Apache).</span></li>
      <li>Preparado para ejecutar despliegues de git automatizado.</li>
      <li>No hay dependencias externas (tal vez a veces se rompen).</li>
      <li>Desarrollado para que sea extremadamente fácil para usted para eliminar las características o añadir sus propias cosas.</li>
    </ul>

